package Dto;

/**
 *
 * @author Lucas
 */
public class monedaDTO {
    
    private int id;
    private String nombre;
    private float valor;

    public monedaDTO() {
    }

    public monedaDTO(int id, String nombre, float valor) {
        this.id = id;
        this.nombre = nombre;
        this.valor = valor;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public float getValor() {
        return valor;
    }

    public void setValor(float valor) {
        this.valor = valor;
    }
    
    
           
    
}
